package day08_loops;

public class JavaLoop {

	public static void main(String[] args) {
		
		for (int i = 20; i <= 30; i++) {
			System.out.println(i);
		}

	System.out.println("-------------");
	    int i = 20;
	    while (i <= 30) {
	    	System.out.println(i);
	        i++;
	    }
	System.out.println("--------------");
	    
	     i = 20;
	
	    do {
	    	System.out.println(i);
	        i++;
	    
	    }while (i<=30);
	     
	
	}

}
